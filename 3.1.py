"Напишіть програму, яка допомагає знайти число (НСК - найменшеспільне кратне) двох чисел. Програма повинна зчитувати два додатних цілих числа a і b (кожне число вводиться на окремомурядку) і виводити найменше число, яке ділиться на обидва цих числа без залишку. НСК(a, b) = |ab| / НСД(a, b), де НСД(a, b) - найбільшийспільний дільник чисел a, b. Виконав Петюх О.В"
# Зчитуємо два додатні цілі числа
a = int(input("Введіть перше число (a): "))
b = int(input("Введіть друге число (b): "))

# Знаходимо НСД за допомогою алгоритму Евкліда
while b:
    a, b = b, a % b

# Знаходимо та виводимо НСК
lcm = abs(a)  # НСК(a, b) = |ab| / НСД(a, b)
print(f"Найменше спільне кратне (НСК) для {a} і {b} = {lcm}")
