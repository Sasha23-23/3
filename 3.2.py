"Є монотонна послідовність, в якій кожне натуральне число k зустрічається рівно k разів: 1, 2, 2, 3, 3, 3, 4, 4, 4, 4, .... За введеним натуральним числом n виведіть перші n членів цієї послідовності. Виконав Петюх О.В"
n = int(input("Введіть кількість членів послідовності (n): "))

sequence = []
num = 1

for k in range(1, n + 1):
    for _ in range(k):
        sequence.append(num)
        if len(sequence) == n:
            break
    num += 1

print(f"Перші {n} членів послідовності: {sequence}")
